window.survey = new Survey.Model(json);

survey
    .onComplete
    .add(function (result) {
        var EI,SN,TF,JP;

        var EIa = 0;
        var EIb = 0;

        var SNa = 0;
        var SNb = 0;

        var TFa = 0;
        var TFb = 0;

        var JPa = 0;
        var JPb = 0;

        for (var i=1; i<=36; i++) {

            var idx = i.toString();
            var condition = i % 4;

            if (JSON.stringify(result.data[idx], null, 3) == "\"A\"") {
                switch(condition) {
                    case 1: EIa++;
                        break;
                    case 2: SNa++;
                        break;
                    case 3: TFa++;
                        break;
                    case 4: JPa++;
                        break;
                }
            }
            else if (JSON.stringify(result.data[idx], null, 3) == "\"B\"") {
                switch(condition) {
                    case 1: EIb++;
                        break;
                    case 2: SNb++;
                        break;
                    case 3: TFb++;
                        break;
                    case 4: JPb++;
                        break;
                }
            }

        }

        if (EIa > EIb) EI = "E"
        else EI = "I"

        if (SNa > SNb) SN = "S"
        else SN = "N"

        if (TFa > TFb) TF = "T"
        else TF = "F"

        if (JPa > JPb) JP = "J"
        else JP = "P"

        document
            .querySelector('#surveyResult')
            .textContent = EI + SN + TF + JP;
    });

$("#surveyElement").Survey({model: survey});